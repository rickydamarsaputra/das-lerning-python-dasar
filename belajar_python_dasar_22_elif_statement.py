# ELIF = else if statement

nama = input("Masukan nama anda: ")

if nama == "ricky": # kondisi 1
  print(f"Selamat siang {nama}") # aksi true 1
elif nama == "rifki": # kondisi 2
  print(f"Selamat malam {nama}") # aksi true 2
else:
  print("Siapa anda?") # aksi false

print("Ini adalah akhir dari program")