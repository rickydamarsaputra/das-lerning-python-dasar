# list -> array, mengakses data menggunakan index
data_list = ["ricky", "damar", "saputra"]

print(data_list[0])

# dictionary (dict) -> associative array
# identifier -> key

data_dict = {
  'cp': 'ricky',
  'tg': 'damar',
  'sa': 'saputra',
  'nmbr': 2001,
  'list': data_list
}

print(data_dict['cp'])
print(data_dict['nmbr'])
print(data_dict['list'])